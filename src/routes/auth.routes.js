/**
 * @swagger
 * /api/auth/profile:
 *   get:
 *     summary: Get user profile and dashboard information
 *     tags: [Authentication]
 *     security:
 *       - bearerAuth: []
 *     responses:
 *       200:
 *         description: User profile retrieved successfully
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 success:
 *                   type: boolean
 *                   example: true
 *                 data:
 *                   type: object
 *                   properties:
 *                     user:
 *                       type: object
 *                       properties:
 *                         _id:
 *                           type: string
 *                         email:
 *                           type: string
 *                         firstName:
 *                           type: string
 *                         lastName:
 *                           type: string
 *                         role:
 *                           type: string
 *                         phone:
 *                           type: string
 *                         photo:
 *                           type: string
 *                         lastLogin:
 *                           type: string
 *                     dashboard:
 *                       type: object
 *                       properties:
 *                         redirectTo:
 *                           type: string
 *                           example: "/admin/dashboard"
 *                         role:
 *                           type: string
 *                           example: "admin"
 *                         permissions:
 *                           type: array
 *                           items:
 *                             type: string
 *       401:
 *         description: Unauthorized
 */
router.get('/profile', auth, async (req, res) => {
  try {
    const user = await User.findById(req.user._id).select('-password');
    
    if (!user) {
      return res.status(404).json({
        success: false,
        message: 'User not found'
      });
    }

    // Determine dashboard redirect based on role
    let redirectTo = '/visitor/dashboard';
    let permissions = ['view_visits', 'create_visits'];

    switch (user.role) {
      case 'admin':
        redirectTo = '/admin/dashboard';
        permissions = [
          'view_all_visitors',
          'check_in_visitors',
          'check_out_visitors',
          'manage_visitors',
          'view_reports',
          'manage_users'
        ];
        break;
      case 'super_admin':
        redirectTo = '/super-admin/dashboard';
        permissions = [
          'view_all_visitors',
          'check_in_visitors',
          'check_out_visitors',
          'manage_visitors',
          'view_reports',
          'manage_users',
          'manage_admins',
          'system_settings'
        ];
        break;
      default:
        redirectTo = '/visitor/dashboard';
        permissions = ['view_visits', 'create_visits'];
    }

    res.json({
      success: true,
      data: {
        user,
        dashboard: {
          redirectTo,
          role: user.role,
          permissions
        }
      }
    });
  } catch (error) {
    res.status(500).json({
      success: false,
      message: 'Error fetching user profile',
      error: error.message
    });
  }
});



